   static void userFDump(int[][] future, int M, int N){
      for (int i = 0; i < M; i++){
            for (int j = 0; j < N; j++){
                if(i == 0 || i == M){
                  future[i][j] = 0;
                }
                if(j == 0 || N == 0){
                  future[i][j] = 0;
                }
                if (future[i][j] == 0)
                    System.out.print(".");
                else
                    System.out.print("*");
            }
            System.out.println();
        }
   }
   
   static void hexDump(int[][] future, int M, int N){
      for(int i = 0; i < M; i++){
         String str = ""; 
         for(int j = 0; j < N; j++){
            str = str + future[i][j];
         }
         String hexNum = new BigInteger(str, 2).toString(16);
         while(hexNum.length() != 8){
            hexNum = '0' + hexNum;
         }
         System.out.println(hexNum); 
      }
   }
   
   static void nextGeneration(int grid[][], int M, int N){
        int[][] future = new int[M][N];

        for (int l = 1; l < M - 1; l++)
        {
            for (int m = 1; m < N - 1; m++)
            {
                // # of living neighbors
                int aliveN = 0;
                for (int i = -1; i <= 1; i++){
                    for (int j = -1; j <= 1; j++){
                        aliveN += grid[l + i][m + j];
                    }
                }
                aliveN -= grid[l][m];

                // living cell has less than 2 living neighbors --> dies
                if ((grid[l][m] == 1) && (aliveN < 2))
                    future[l][m] = 0;
  
                // living cell has more than 3 living neighbors --> dies
                else if ((grid[l][m] == 1) && (aliveN > 3))
                    future[l][m] = 0;
  
                // dead cell has exactly 3 living neighbors --> comes to life
                else if ((grid[l][m] == 0) && (aliveN == 3))
                    future[l][m] = 1;
  
                //doesnt change 
                else
                    future[l][m] = grid[l][m];
            }
        }
        userFDump(future, M, N);
        hexDump(future, M, N); 
    }
